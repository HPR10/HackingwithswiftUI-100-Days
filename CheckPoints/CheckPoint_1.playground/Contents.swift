import Cocoa

// Criar uma constante de qualquer temperatura em Celsius.

let celsius: Double = 23.0

// Converte-o em Fahrenheit multiplicando por 9 e devidindo por 5 e somando 32

let fahrenheit: Double = 23 * 9 / 5 + 32

// Imprima o resultado para o usuário e mostre o valor de Celsius e Fahrenheit
print("O valor em Celsius é: \(celsius) e o valor em Fahrenheit é \(fahrenheit)")
