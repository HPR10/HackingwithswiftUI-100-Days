// MARK: - Strings

// Strings começam e terminam com "".
let actor = "Hugo Pinheiro"

// Strings aceitam emojs 🥰.
let pet = "Jao Pinheiro 🐶"

// Colocar aspas na String
let frase = "Eu sou ser um grande \"Desenvolvedor\" da platlaforma ios"

// váriáveis ou constantes em mais de uma linha.
var filmes = """
Harry potter
e o cálice de fogo
"""

// MARK: - Métodos para Strings

// Count: Método de contar quantas letras tem em uma string.
print(filmes.count)

// hasPrefix and hasSuffix: Verifica se uma string começa ou termina com letras específicas com retorno bolleano.
print(actor.hasPrefix("Hu"))
print(pet.hasSuffix("🐶"))


